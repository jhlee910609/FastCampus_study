package com.junhee.java.obj;

public class People {

	private String gender;
	private int age;
	private String skinColor;
	private String hairColor;
	private int height;
	private int weight;
	private String name;

	public void move(int k) {
		System.out.println(k + "으로 이동 중입니다.");
	}

	// private으로 막힌 정보를 가져오는 메소드 생성
	// private으로 불러 올 수 없는 정보를 가져올 때.
	// 직접 호출할 때보다 조금 느려지는 경향이 있음
	public String getName() {
		return name;
	}

	// 직접 호출할 때보다 조금 느려지는 경향이 있음
	//
	public void setName(String value) {
		name = value;
	}

}
